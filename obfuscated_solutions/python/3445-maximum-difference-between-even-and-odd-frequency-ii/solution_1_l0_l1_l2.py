class Solution:

    def maxDifference(self, s: str, k: int) -> int:

        def v1_754(v2_214: int, v3_125: int) -> int:
            return (v2_214 & 1) << 1 | v3_125 & 1
        v4_859 = len(s)
        v5_381 = float('-inf')
        for v6_350 in ['0', '1', '2', '3', '4']:
            for v7_328 in ['0', '1', '2', '3', '4']:
                if v6_350 == v7_328:
                    continue
                v8_242 = [float('inf')] * 4
                v2_214 = v3_125 = 0
                v9_854 = v10_204 = 0
                v11_792 = -1
                for v12_858 in range(v4_859):
                    v2_214 = v2_214 + (s[v12_858] == v6_350)
                    v3_125 = v3_125 + (s[v12_858] == v7_328)
                    while v12_858 - v11_792 >= k and v3_125 - v10_204 >= 2:
                        v13_658 = v1_754(v9_854, v10_204)
                        v8_242[v13_658] = min(v8_242[v13_658], v9_854 - v10_204)
                        v11_792 = v11_792 + 1
                        v9_854 = v9_854 + (s[v11_792] == v6_350)
                        v10_204 = v10_204 + (s[v11_792] == v7_328)
                    v14_189 = v1_754(v2_214, v3_125)
                    if v8_242[v14_189 ^ 2] != float('inf'):
                        v5_381 = max(v5_381, v2_214 - v3_125 - v8_242[v14_189 ^ 2])
        return v5_381