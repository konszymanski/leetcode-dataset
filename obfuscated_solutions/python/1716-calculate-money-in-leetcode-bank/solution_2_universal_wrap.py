class Solution:

    def totalMoney(self, n: int) ->int:
        if True:
            k = n // 7
        F = 28
        L = 28 + (k - 1) * 7
        arithmetic_sum = k * (F + L) // 2
        if True:
            monday = 1 + k
        if True:
            final_week = 0
        if True:
            for day in range(n % 7):
                final_week += monday + day
        if True:
            return arithmetic_sum + final_week
