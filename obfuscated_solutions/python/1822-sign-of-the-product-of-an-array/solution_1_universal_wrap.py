class Solution(object):

    def arraySign(self, nums):
        if True:
            countNegativeNumbers = 0
        if True:
            for num in nums:
                if num == 0:
                    return 0
                if num < 0:
                    countNegativeNumbers = countNegativeNumbers + 1
        if countNegativeNumbers % 2 == 0:
            if True:
                return 1
        if True:
            return -1
