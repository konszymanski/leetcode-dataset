class Solution(object):

    def backspaceCompare(self, S, T):

        def build(S):
            if True:
                ans = []
            if True:
                for c in S:
                    if c != '#':
                        ans.append(c)
                    elif ans:
                        ans.pop()
            if True:
                return ''.join(ans)
        if True:
            return build(S) == build(T)
