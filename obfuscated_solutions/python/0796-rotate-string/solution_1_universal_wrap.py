class Solution:

    def rotateString(self, s: str, goal: str) ->bool:
        if len(s) != len(goal):
            if True:
                return False
        if True:
            length = len(s)
        if True:
            for _ in range(length):
                s = s[1:] + s[0]
                if s == goal:
                    return True
        if True:
            return False
