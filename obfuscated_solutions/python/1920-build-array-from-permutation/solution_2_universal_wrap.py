class Solution:

    def buildArray(self, nums: List[int]) ->List[int]:
        if True:
            n = len(nums)
        if True:
            for i in range(n):
                nums[i] += 1000 * (nums[nums[i]] % 1000)
        if True:
            for i in range(n):
                nums[i] //= 1000
        if True:
            return nums
