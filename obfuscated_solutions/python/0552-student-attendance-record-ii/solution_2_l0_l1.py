class Solution:
    def checkRecord(self, n: int) -> int:
        v1_754 = 1000000007
        v2_214 = [[[0] * 3 for v3_125 in range(2)] for v3_125 in range(n  +  1)]
        v2_214[0][0][0] = 1
        for v4_859 in range(n):
            for v5_381 in range(2):
                for v6_350 in range(3):
                    v2_214[v4_859  +  1][v5_381][0] = (
                        v2_214[v4_859  +  1][v5_381][0]
                        + v2_214[v4_859][v5_381][v6_350]
                    ) % v1_754
                    if v5_381 < 1:
                        v2_214[v4_859  +  1][v5_381  +  1][0] = (
                            v2_214[v4_859  +  1][v5_381  +  1][0]
                            + v2_214[v4_859][v5_381][v6_350]
                        ) % v1_754
                    if v6_350 < 2:
                        v2_214[v4_859  +  1][v5_381][
                            v6_350  +  1
                        ] = (
                            v2_214[v4_859 + 1][v5_381][
                                v6_350 + 1
                            ]
                            + v2_214[v4_859][v5_381][v6_350]
                        ) % v1_754
        v7_328 = 0
        for v5_381 in range(2):
            for v6_350 in range(3):
                v7_328 = (v7_328  +  v2_214[n][v5_381][v6_350]) % v1_754
        return v7_328
