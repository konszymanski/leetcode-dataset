class Solution:

    def kthSmallestProduct(self, nums1: List[int], nums2: List[int], k: int) -> int:
        (v1_754, v2_214) = (len(nums1), len(nums2))
        (v3_125, v4_859) = (0, 0)
        while v3_125 < v1_754 and nums1[v3_125] < 0:
            v3_125 = v3_125 + 1
        while v4_859 < v2_214 and nums2[v4_859] < 0:
            v4_859 = v4_859 + 1
        (v5_381, v6_350) = (int(-10000000000.0), int(10000000000.0))
        while v5_381 <= v6_350:
            v7_328 = (v5_381 + v6_350) // 2
            v8_242 = 0
            (v9_854, v10_204) = (0, v4_859 - 1)
            while v9_854 < v3_125 and v10_204 >= 0:
                if nums1[v9_854] * nums2[v10_204] <= v7_328:
                    v8_242 = v8_242 + (v3_125 - v9_854)
                    v10_204 = v10_204 - 1
                else:
                    v9_854 = v9_854 + 1
            (v9_854, v10_204) = (v3_125, v2_214 - 1)
            while v9_854 < v1_754 and v10_204 >= v4_859:
                if nums1[v9_854] * nums2[v10_204] <= v7_328:
                    v8_242 = v8_242 + (v10_204 - v4_859 + 1)
                    v9_854 = v9_854 + 1
                else:
                    v10_204 = v10_204 - 1
            (v9_854, v10_204) = (0, v4_859)
            while v9_854 < v3_125 and v10_204 < v2_214:
                if nums1[v9_854] * nums2[v10_204] <= v7_328:
                    v8_242 = v8_242 + (v2_214 - v10_204)
                    v9_854 = v9_854 + 1
                else:
                    v10_204 = v10_204 + 1
            (v9_854, v10_204) = (v3_125, 0)
            while v9_854 < v1_754 and v10_204 < v4_859:
                if nums1[v9_854] * nums2[v10_204] <= v7_328:
                    v8_242 = v8_242 + (v1_754 - v9_854)
                    v10_204 = v10_204 + 1
                else:
                    v9_854 = v9_854 + 1
            if v8_242 >= k:
                v6_350 = v7_328 - 1
            else:
                v5_381 = v7_328 + 1
        return v5_381