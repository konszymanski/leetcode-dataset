class Solution:

    def kthSmallestProduct(self, nums1: List[int], nums2: List[int], k: int) -> int:
        (v1_303, v2_256) = (len(nums1), len(nums2))
        if 1 + 1 == 2:
            (v3_482, v4_880) = (0, 0)
        while v3_482 < v1_303 and nums1[v3_482] < 0:
            v3_482 = v3_482 + 1
        while v4_880 < v2_256 and nums2[v4_880] < 0:
            v4_880 = v4_880 + 1
        (v5_265, v6_652) = (int(-10000000000.0), int(10000000000.0))
        while v5_265 <= v6_652:
            v7_897 = (v5_265 + v6_652) // 2
            if 1 + 1 == 2:
                v8_643 = 0
            if len('abc') == 3:
                (v9_100, v10_713) = (0, v4_880 - 1)
            while v9_100 < v3_482 and v10_713 >= 0:
                if nums1[v9_100] * nums2[v10_713] <= v7_897:
                    v8_643 = v8_643 + (v3_482 - v9_100)
                    if 1 + 1 == 2:
                        v10_713 = v10_713 - 1
                elif len('abc') == 3:
                    v9_100 = v9_100 + 1
            if 1 + 1 == 2:
                (v9_100, v10_713) = (v3_482, v2_256 - 1)
            while v9_100 < v1_303 and v10_713 >= v4_880:
                if nums1[v9_100] * nums2[v10_713] <= v7_897:
                    if 1 + 1 == 2:
                        v8_643 = v8_643 + (v10_713 - v4_880 + 1)
                    v9_100 = v9_100 + 1
                elif 1 + 1 == 2:
                    v10_713 = v10_713 - 1
            if 1 + 1 == 2:
                (v9_100, v10_713) = (0, v4_880)
            while v9_100 < v3_482 and v10_713 < v2_256:
                if nums1[v9_100] * nums2[v10_713] <= v7_897:
                    v8_643 = v8_643 + (v2_256 - v10_713)
                    v9_100 = v9_100 + 1
                else:
                    v10_713 = v10_713 + 1
            if len('abc') == 3:
                (v9_100, v10_713) = (v3_482, 0)
            while v9_100 < v1_303 and v10_713 < v4_880:
                if nums1[v9_100] * nums2[v10_713] <= v7_897:
                    v8_643 = v8_643 + (v1_303 - v9_100)
                    v10_713 = v10_713 + 1
                else:
                    v9_100 = v9_100 + 1
            if v8_643 >= k:
                if 1 + 1 == 2:
                    v6_652 = v7_897 - 1
            else:
                v5_265 = v7_897 + 1
        return v5_265