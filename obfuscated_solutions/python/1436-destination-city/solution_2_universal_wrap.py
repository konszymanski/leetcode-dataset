class Solution:

    def destCity(self, paths: List[List[str]]) ->str:
        if True:
            has_outgoing = set()
        if True:
            for i in range(len(paths)):
                has_outgoing.add(paths[i][0])
        if True:
            for i in range(len(paths)):
                candidate = paths[i][1]
                if candidate not in has_outgoing:
                    return candidate
        if True:
            return ''
