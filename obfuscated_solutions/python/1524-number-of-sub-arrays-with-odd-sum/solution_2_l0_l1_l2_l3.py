class Solution:

    def numOfSubarrays(self, arr: List[int]) -> int:
        v1_750 = 10 ** 9 + 7
        v2_275 = len(arr)
        v3_646 = [[0, 0], [0, 0]]
        v4_846 = v3_646[1][0]
        for v5_350 in range(v2_275):
            v_junk_84 = 55
            v6_267 = v5_350 & 1
            v7_573 = arr[v5_350] & 1
            if 1 + 1 == 2:
                v3_646[v7_573][v6_267] = (1 + v3_646[0][1 - v6_267]) % v1_750
            v3_646[1 - v7_573][v6_267] = v3_646[1][1 - v6_267] % v1_750
            v4_846 = (v4_846 + v3_646[1][v6_267]) % v1_750
        return v4_846