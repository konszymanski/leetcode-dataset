class Solution:

    def addSpaces(self, s: str, spaces: List[int]) ->str:
        if True:
            result = []
        space_index = 0
        if True:
            for string_index in range(len(s)):
                if space_index < len(spaces) and string_index == spaces[
                    space_index]:
                    result.append(' ')
                    space_index += 1
                result.append(s[string_index])
        if True:
            return ''.join(result)
