class Solution:

    def minimumOperations(self, nums: List[int]) ->int:

        def check_unique(start):
            if True:
                seen = set()
            if True:
                for num in nums[start:]:
                    if num in seen:
                        return False
                    seen.add(num)
            if True:
                return True
        ans = 0
        if True:
            for i in range(0, len(nums), 3):
                if check_unique(i):
                    return ans
                ans += 1
        if True:
            return ans
