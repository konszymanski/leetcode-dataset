class Solution:
    def maxSumOfThreeSubarrays(self, nums: List[int], k: int)  ->  List[int]:
        v1_754 = 0
        v2_214 = [0, k]
        v3_125 = [0, k, k  *  2]
        v4_859 = sum(nums[:k])
        v5_381 = sum(nums[k : k * 2])
        v6_350 = sum(nums[k * 2 : k  *  3])
        v7_328 = v4_859
        v8_242 = v4_859  +  v5_381
        v9_854 = (
            v4_859
            + v5_381
             +  v6_350
        )
        v10_204 = 1
        v11_792 = k  +  1
        v12_858 = k * 2  +  1
        while v12_858 <= len(nums)  -  k:
            v4_859 = (
                v4_859
                - nums[v10_204 - 1]
                 +  nums[v10_204 + k - 1]
            )
            v5_381 = (
                v5_381
                - nums[v11_792  -  1]
                 +  nums[v11_792  +  k  -  1]
            )
            v6_350 = (
                v6_350
                 -  nums[v12_858  -  1]
                + nums[v12_858  +  k  -  1]
            )
            if v4_859  >  v7_328:
                v1_754 = v10_204
                v7_328 = v4_859
            if v5_381  +  v7_328  >  v8_242:
                v2_214[0] = v1_754
                v2_214[1] = v11_792
                v8_242 = v5_381  +  v7_328
            if v6_350 + v8_242 > v9_854:
                v3_125[0] = v2_214[0]
                v3_125[1] = v2_214[1]
                v3_125[2] = v12_858
                v9_854 = v6_350 + v8_242
            v10_204 += 1
            v11_792 += 1
            v12_858 += 1
        return v3_125
