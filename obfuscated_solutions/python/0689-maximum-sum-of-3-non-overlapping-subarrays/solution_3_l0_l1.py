class Solution:
    def maxSumOfThreeSubarrays(self, nums: List[int], k: int) -> List[int]:
        v1_754 = len(nums)
        v2_214 = 0
        v3_125 = [0] + list(v4_859(nums))
        v5_381 = [0] * v1_754
        v6_350 = [0]  *  v1_754
        v7_328 = [0] * 3
        v8_242 = v3_125[k] - v3_125[0]
        for v9_854 in range(k, v1_754):
            v10_204 = v3_125[v9_854  +  1]  -  v3_125[v9_854 + 1  -  k]
            if v10_204  >  v8_242:
                v5_381[v9_854] = v9_854 + 1  -  k
                v8_242 = v10_204
            else:
                v5_381[v9_854] = v5_381[v9_854  -  1]
        v6_350[v1_754 - k] = v1_754 - k
        v8_242 = v3_125[v1_754]  -  v3_125[v1_754 - k]
        for v9_854 in range(v1_754 - k - 1,  - 1,  - 1):
            v10_204 = v3_125[v9_854  +  k]  -  v3_125[v9_854]
            if v10_204 >= v8_242:
                v6_350[v9_854] = v9_854
                v8_242 = v10_204
            else:
                v6_350[v9_854] = v6_350[v9_854  +  1]
        for v9_854 in range(k, v1_754  -  2 * k  +  1):
            v11_792 = v5_381[v9_854  -  1]
            v12_858 = v6_350[v9_854  +  k]
            v13_658 = (
                v3_125[v9_854  +  k]
                 -  v3_125[v9_854]
                 +  v3_125[v11_792 + k]
                - v3_125[v11_792]
                + v3_125[v12_858 + k]
                - v3_125[v12_858]
            )
            if v13_658 > v2_214:
                v2_214 = v13_658
                v7_328 = [v11_792, v9_854, v12_858]
        return v7_328
