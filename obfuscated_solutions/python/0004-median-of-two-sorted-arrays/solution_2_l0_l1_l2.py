class Solution:

    def findMedianSortedArrays(self, A: List[int], B: List[int]) -> float:
        (v1_754, v2_214) = (len(A), len(B))
        v3_125 = v1_754 + v2_214

        def v4_859(v5_381, v6_350, v7_328, v8_242, v9_854):
            if v6_350 > v7_328:
                return B[v5_381 - v6_350]
            if v8_242 > v9_854:
                return A[v5_381 - v8_242]
            (v10_204, v11_792) = ((v6_350 + v7_328) // 2, (v8_242 + v9_854) // 2)
            (v12_858, v13_658) = (A[v10_204], B[v11_792])
            if v10_204 + v11_792 >= v5_381:
                if v12_858 <= v13_658:
                    return v4_859(v5_381, v6_350, v7_328, v8_242, v11_792 - 1)
                else:
                    return v4_859(v5_381, v6_350, v10_204 - 1, v8_242, v9_854)
            elif v12_858 <= v13_658:
                return v4_859(v5_381, v10_204 + 1, v7_328, v8_242, v9_854)
            else:
                return v4_859(v5_381, v6_350, v7_328, v11_792 + 1, v9_854)
        if v3_125 % 2:
            return v4_859(v3_125 // 2, 0, v1_754 - 1, 0, v2_214 - 1)
        else:
            return (v4_859(v3_125 // 2 - 1, 0, v1_754 - 1, 0, v2_214 - 1) + v4_859(v3_125 // 2, 0, v1_754 - 1, 0, v2_214 - 1)) / 2