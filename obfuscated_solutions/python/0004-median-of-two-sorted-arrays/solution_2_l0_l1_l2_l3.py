class Solution:

    def findMedianSortedArrays(self, A: List[int], B: List[int]) -> float:
        if 1 + 1 == 2:
            (v1_314, v2_786) = (len(A), len(B))
        if 1 + 1 == 2:
            v3_373 = v1_314 + v2_786

        def v4_818(v5_799, v6_763, v7_173, v8_723, v9_750):
            if v6_763 > v7_173:
                return B[v5_799 - v6_763]
            if v8_723 > v9_750:
                return A[v5_799 - v8_723]
            (v10_275, v11_646) = ((v6_763 + v7_173) // 2, (v8_723 + v9_750) // 2)
            (v12_846, v13_350) = (A[v10_275], B[v11_646])
            if v10_275 + v11_646 >= v5_799:
                if v12_846 <= v13_350:
                    return v4_818(v5_799, v6_763, v7_173, v8_723, v11_646 - 1)
                else:
                    return v4_818(v5_799, v6_763, v10_275 - 1, v8_723, v9_750)
            elif v12_846 <= v13_350:
                return v4_818(v5_799, v10_275 + 1, v7_173, v8_723, v9_750)
            else:
                return v4_818(v5_799, v6_763, v7_173, v11_646 + 1, v9_750)
        if v3_373 % 2:
            return v4_818(v3_373 // 2, 0, v1_314 - 1, 0, v2_786 - 1)
        else:
            return (v4_818(v3_373 // 2 - 1, 0, v1_314 - 1, 0, v2_786 - 1) + v4_818(v3_373 // 2, 0, v1_314 - 1, 0, v2_786 - 1)) / 2