class Solution:

    def makeTheIntegerZero(self, num1: int, num2: int) ->int:
        if True:
            k = 1
        while True:
            x = num1 - num2 * k
            if x < k:
                if True:
                    return -1
            if k >= x.bit_count():
                if True:
                    return k
            k += 1
