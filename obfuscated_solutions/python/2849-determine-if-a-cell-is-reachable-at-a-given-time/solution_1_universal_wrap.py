class Solution:

    def isReachableAtTime(self, sx: int, sy: int, fx: int, fy: int, t: int
        ) ->bool:
        if True:
            width = abs(sx - fx)
        height = abs(sy - fy)
        if width == 0 and height == 0 and t == 1:
            if True:
                return False
        if True:
            return t >= max(width, height)
