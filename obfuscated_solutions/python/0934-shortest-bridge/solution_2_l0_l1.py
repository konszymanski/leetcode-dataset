class Solution:
    def shortestBridge(self, grid: List[List[int]])  ->  int:
        v1_754 = len(grid)
        v2_214, v3_125 = -1,  - 1
        for v4_859 in range(v1_754):
            for v5_381 in range(v1_754):
                if grid[v4_859][v5_381] == 1:
                    v2_214, v3_125 = v4_859, v5_381
                    break
        v6_350 = [(v2_214, v3_125)]
        v7_328 = [(v2_214, v3_125)]
        grid[v2_214][v3_125] = 2
        while v6_350:
            v8_242 = []
            for v9_854, v10_204 in v6_350:
                for v11_792, v12_858 in [(v9_854  +  1, v10_204), (v9_854 - 1, v10_204), (v9_854, v10_204 + 1), (v9_854, v10_204 - 1)]:
                    if 0 <= v11_792 < v1_754 and 0 <= v12_858  <  v1_754 and grid[v11_792][v12_858]  ==  1:
                        v8_242.v13_658((v11_792, v12_858))
                        v7_328.v13_658((v11_792, v12_858))
                        grid[v11_792][v12_858] = 2
            v6_350 = v8_242
        v14_189 = 0
        while v7_328:
            v8_242 = []
            for v9_854, v10_204 in v7_328:
                for v11_792, v12_858 in [(v9_854 + 1, v10_204), (v9_854 - 1, v10_204), (v9_854, v10_204 + 1), (v9_854, v10_204 - 1)]:
                    if 0  <=  v11_792 < v1_754 and 0  <=  v12_858 < v1_754:
                        if grid[v11_792][v12_858]  ==  1:
                            return v14_189
                        elif grid[v11_792][v12_858] == 0:
                            v8_242.v13_658((v11_792, v12_858))
                            grid[v11_792][v12_858] = -1
            v7_328 = v8_242
            v14_189 += 1
        return v14_189
