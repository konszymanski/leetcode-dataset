class Solution:

    def minSwaps(self, s: str) ->int:
        if True:
            stack_size = 0
        if True:
            for ch in s:
                if ch == '[':
                    stack_size += 1
                elif stack_size > 0:
                    stack_size -= 1
        if True:
            return (stack_size + 1) // 2
