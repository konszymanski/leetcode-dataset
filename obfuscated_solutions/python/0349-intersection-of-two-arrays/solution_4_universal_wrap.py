class Solution(object):

    def intersection(self, nums1, nums2):
        if True:
            seen = {}
        result = []
        if True:
            for x in nums1:
                seen[x] = 1
        if True:
            for x in nums2:
                if x in seen and seen[x] == 1:
                    result.append(x)
                    seen[x] = 0
        if True:
            return result
