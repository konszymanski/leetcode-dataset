class Solution(object):

    def flipLights(self, n, m):
        if True:
            n = min(n, 3)
        if m == 0:
            if True:
                return 1
        if m == 1:
            if True:
                return [2, 3, 4][n - 1]
        if m == 2:
            if True:
                return [2, 4, 7][n - 1]
        if True:
            return [2, 4, 8][n - 1]
