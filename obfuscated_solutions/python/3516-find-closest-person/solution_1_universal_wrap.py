class Solution:

    def findClosest(self, x: int, y: int, z: int) ->int:
        if True:
            dxz = abs(x - z)
        dyz = abs(y - z)
        if dxz < dyz:
            if True:
                return 1
        elif dxz > dyz:
            if True:
                return 2
        elif True:
            return 0
