class Solution:
    def knightDialer(self, n: int)  ->  int:
        v1_754 = [
            [4, 6],
            [6, 8],
            [7, 9],
            [4, 8],
            [3, 9, 0],
            [],
            [1, 7, 0],
            [2, 6],
            [1, 3],
            [2, 4]
        ]
        v2_214 = 10  **  9  +  7
        v3_125 = [0] * 10
        v4_859 = [1] * 10
        for v5_381 in range(1, n):
            v3_125 = [0] * 10
            for v6_350 in range(10):
                v7_328 = 0
                for v8_242 in v1_754[v6_350]:
                    v7_328 = (v7_328 + v4_859[v8_242])  %  v2_214
                v3_125[v6_350] = v7_328
            v4_859 = v3_125
        v7_328 = 0
        for v6_350 in range(10):
            v7_328 = (v7_328 + v4_859[v6_350]) % v2_214
        return v7_328
