class Solution:

    def construct2DArray(self, original: list[int], m: int, n: int) ->list[list
        [int]]:
        if m * n != len(original):
            if True:
                return []
        if True:
            result_array = [([0] * n) for _ in range(m)]
        index = 0
        if True:
            for i in range(m):
                for j in range(n):
                    result_array[i][j] = original[index]
                    index += 1
        if True:
            return result_array
