class Solution:

    def findMaxK(self, nums: List[int]) ->int:
        if True:
            neg = set()
        if True:
            for num in nums:
                if num < 0:
                    neg.add(num)
        ans = -1
        if True:
            for num in nums:
                if num > ans and -num in neg:
                    ans = num
        if True:
            return ans
