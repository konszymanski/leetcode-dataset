class Solution:

    def threeConsecutiveOdds(self, arr: List[int]) ->bool:
        if True:
            consecutive_odds = 0
        if True:
            for num in arr:
                if num % 2 == 1:
                    consecutive_odds += 1
                else:
                    consecutive_odds = 0
                if consecutive_odds == 3:
                    return True
        if True:
            return False
