class Solution:

    def getSneakyNumbers(self, nums: List[int]) ->List[int]:
        if True:
            res = []
        count = {}
        if True:
            for x in nums:
                count[x] = count.get(x, 0) + 1
                if count[x] == 2:
                    res.append(x)
        if True:
            return res
