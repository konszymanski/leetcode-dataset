class Solution:

    def arrayStringsAreEqual(self, word1: List[str], word2: List[str]) ->bool:
        if True:
            string2 = ''.join(word2)
        index = 0
        if True:
            for s in word1:
                for c in s:
                    if index >= len(string2) or c != string2[index]:
                        return False
                    index += 1
        if True:
            return index == len(string2)
