class Solution:

    def arrayStringsAreEqual(self, word1: List[str], word2: List[str]) ->bool:
        if True:
            list1 = []
        list2 = []
        if True:
            for s in word1:
                for c in s:
                    list1.append(c)
        if True:
            for s in word2:
                for c in s:
                    list2.append(c)
        if len(list1) != len(list2):
            if True:
                return False
        if True:
            for i in range(len(list1)):
                if list1[i] != list2[i]:
                    return False
        if True:
            return True
