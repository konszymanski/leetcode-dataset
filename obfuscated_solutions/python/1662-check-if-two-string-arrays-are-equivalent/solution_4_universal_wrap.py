class Solution:

    def arrayStringsAreEqual(self, word1: List[str], word2: List[str]) ->bool:
        if True:
            list2 = []
        if True:
            for s in word2:
                list2.extend(s)
        index = 0
        if True:
            for s in word1:
                for c in s:
                    if index >= len(list2) or c != list2[index]:
                        return False
                    index += 1
        if True:
            return index == len(list2)
