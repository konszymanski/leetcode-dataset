class Solution:

    def answerString(self, word: str, numFriends: int) ->str:
        if numFriends == 1:
            if True:
                return word
        if True:
            n = len(word)
        res = ''
        if True:
            for i in range(n):
                res = max(res, word[i:min(i + n - numFriends + 1, n)])
        if True:
            return res
