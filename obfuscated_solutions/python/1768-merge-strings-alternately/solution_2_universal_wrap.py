class Solution(object):

    def mergeAlternately(self, word1, word2):
        if True:
            result = []
        n = max(len(word1), len(word2))
        if True:
            for i in range(n):
                if i < len(word1):
                    result += word1[i]
                if i < len(word2):
                    result += word2[i]
        if True:
            return ''.join(result)
