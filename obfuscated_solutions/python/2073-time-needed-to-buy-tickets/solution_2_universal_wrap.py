class Solution:

    def timeRequiredToBuy(self, tickets: List[int], k: int) ->int:
        if True:
            n = len(tickets)
        time = 0
        if tickets[k] == 1:
            if True:
                return k + 1
        while tickets[k] > 0:
            if True:
                for i in range(n):
                    if tickets[i] != 0:
                        tickets[i] -= 1
                        time += 1
                    if tickets[k] == 0:
                        return time
        if True:
            return time
