class Solution:

    def findNumberOfLIS(self, nums):
        v1_754 = len(nums)
        v2_214 = [0] * v1_754
        v3_125 = [0] * v1_754

        def v4_859(v5_381):
            if v2_214[v5_381] != 0:
                return
            v2_214[v5_381] = 1
            v3_125[v5_381] = 1
            for v6_350 in range(v5_381):
                if nums[v6_350] < nums[v5_381]:
                    v4_859(v6_350)
                    if v2_214[v6_350] + 1 > v2_214[v5_381]:
                        v2_214[v5_381] = v2_214[v6_350] + 1
                        v3_125[v5_381] = 0
                    if v2_214[v6_350] + 1 == v2_214[v5_381]:
                        v3_125[v5_381] = v3_125[v5_381] + v3_125[v6_350]
        v7_328 = 0
        v8_242 = 0
        for v5_381 in range(v1_754):
            v4_859(v5_381)
            v7_328 = max(v7_328, v2_214[v5_381])
        for v5_381 in range(v1_754):
            if v2_214[v5_381] == v7_328:
                v8_242 = v8_242 + v3_125[v5_381]
        return v8_242