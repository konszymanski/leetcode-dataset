class Solution:

    def triangularSum(self, nums: List[int]) ->int:
        while len(nums) > 1:
            if True:
                new_nums = list()
            if True:
                for i in range(len(nums) - 1):
                    new_nums.append((nums[i] + nums[i + 1]) % 10)
            nums = new_nums
        if True:
            return nums[0]
