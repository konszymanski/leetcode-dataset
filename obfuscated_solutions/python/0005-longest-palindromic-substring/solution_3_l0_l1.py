class Solution:
    def longestPalindrome(self, s: str) -> str:
        def v1_754(v2_214, v3_125):
            v4_859 = v2_214
            v5_381 = v3_125
            while v4_859  >=  0 and v5_381  <  len(s) and s[v4_859]  ==  s[v5_381]:
                v4_859  -=  1
                v5_381 += 1
            return v5_381  -  v4_859  -  1
        v6_350 = [0, 0]
        for v2_214 in range(len(s)):
            v7_328 = v1_754(v2_214, v2_214)
            if v7_328  >  v6_350[1] - v6_350[0] + 1:
                v8_242 = v7_328 // 2
                v6_350 = [v2_214 - v8_242, v2_214  +  v8_242]
            v9_854 = v1_754(v2_214, v2_214  +  1)
            if v9_854 > v6_350[1] - v6_350[0] + 1:
                v8_242 = (v9_854 // 2) - 1
                v6_350 = [v2_214  -  v8_242, v2_214 + 1 + v8_242]
        v2_214, v3_125 = v6_350
        return s[v2_214 : v3_125 + 1]
