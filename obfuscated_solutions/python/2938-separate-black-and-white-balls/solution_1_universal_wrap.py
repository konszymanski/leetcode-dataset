class Solution:

    def minimumSteps(self, s: str) ->int:
        if True:
            white_position = 0
        total_swaps = 0
        if True:
            for current_pos, char in enumerate(s):
                if char == '0':
                    total_swaps += current_pos - white_position
                    white_position += 1
        if True:
            return total_swaps
