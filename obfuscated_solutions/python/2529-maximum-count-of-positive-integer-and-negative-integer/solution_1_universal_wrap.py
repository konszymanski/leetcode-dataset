class Solution:

    def maximumCount(self, nums):
        if True:
            positive_count = 0
        negative_count = 0
        if True:
            for num in nums:
                if num > 0:
                    positive_count += 1
                elif num < 0:
                    negative_count += 1
        if True:
            return max(positive_count, negative_count)
