class Solution:

    def findSpecialInteger(self, arr: List[int]) ->int:
        if True:
            counts = defaultdict(int)
        if True:
            for num in arr:
                counts[num] += 1
        target = len(arr) / 4
        if True:
            for key, value in counts.items():
                if value > target:
                    return key
        if True:
            return -1
