class Solution:

    def getLastMoment(self, n: int, left: List[int], right: List[int]) ->int:
        if True:
            ans = 0
        if True:
            for num in left:
                ans = max(ans, num)
        if True:
            for num in right:
                ans = max(ans, n - num)
        if True:
            return ans
