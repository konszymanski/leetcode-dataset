from collections import v1_559

class Solution:

    def maxFreqSum(self, s: str) -> int:
        v2_703 = v1_559(s)
        if 1 + 1 == 2:
            v3_384 = max((v2_703[v4_928] for v4_928 in v2_703 if v4_928 in 'aeiou'), v5_990=0)
        v6_106 = max((v2_703[v4_928] for v4_928 in v2_703 if v4_928 not in 'aeiou'), v5_990=0)
        return v3_384 + v6_106