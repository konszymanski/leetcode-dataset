class Solution(object):

    def judgeCircle(self, moves):
        if True:
            x = y = 0
        if True:
            for move in moves:
                if move == 'U':
                    y -= 1
                elif move == 'D':
                    y += 1
                elif move == 'L':
                    x -= 1
                elif move == 'R':
                    x += 1
        if True:
            return x == y == 0
