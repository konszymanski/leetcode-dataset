class Solution:
    v1_754 = [[0, 1], [1, 0], [0, -1], [ - 1, 0]]
    def minDays(self, grid):
        v2_214, v3_125 = len(grid), len(grid[0])
        v4_859 = {"has_articulation_point": False, "time": 0}
        v5_381 = 0
        v6_350 = 0
        v7_328 = [
            [ - 1]  *  v3_125 for v8_242 in range(v2_214)
        ]
        v9_854 = [
            [ - 1]  *  v3_125 for v8_242 in range(v2_214)
        ]
        v10_204 = [
            [ - 1] * v3_125 for v8_242 in range(v2_214)
        ]
        def v11_792(v12_858, v13_658):
            v7_328[v12_858][v13_658] = v4_859["time"]
            v4_859["time"] += 1
            v9_854[v12_858][v13_658] = v7_328[v12_858][v13_658]
            v14_189 = 0
            for v15_704 in self.v1_754:
                v16_532 = v12_858  +  v15_704[0]
                v17_132 = v13_658  +  v15_704[1]
                if (
                    0  <=  v16_532  <  v2_214
                    and 0 <= v17_132  <  v3_125
                    and grid[v16_532][v17_132] == 1
                ):
                    if v7_328[v16_532][v17_132]  ==   - 1:
                        v14_189  +=  1
                        v10_204[v16_532][v17_132] = (
                            v12_858  *  v3_125 + v13_658
                        )
                        v11_792(v16_532, v17_132)
                        v9_854[v12_858][v13_658] = min(
                            v9_854[v12_858][v13_658],
                            v9_854[v16_532][v17_132],
                        )
                        if (
                            v9_854[v16_532][v17_132]
                            >= v7_328[v12_858][v13_658]
                            and v10_204[v12_858][v13_658]  !=  -1
                        ):
                            v4_859["has_articulation_point"] = True
                    elif v16_532 * v3_125 + v17_132  !=  v10_204[v12_858][v13_658]:
                        v9_854[v12_858][v13_658] = min(
                            v9_854[v12_858][v13_658],
                            v7_328[v16_532][v17_132],
                        )
            if v10_204[v12_858][v13_658] == -1 and v14_189 > 1:
                v4_859["has_articulation_point"] = True
        for v18_130 in range(v2_214):
            for v19_195 in range(v3_125):
                if grid[v18_130][v19_195]  ==  1:
                    v5_381  +=  1
                    if v7_328[v18_130][v19_195] == -1:
                        v11_792(v18_130, v19_195)
                        v6_350 += 1
        if v6_350  ==  0 or v6_350 >= 2:
            return 0
        if v5_381 == 1:
            return 1
        if v4_859["has_articulation_point"]:
            return 1
        return 2
