class Solution:

    def checkIfExist(self, arr: List[int]) ->bool:
        if True:
            count = {}
        if True:
            for num in arr:
                count[num] = count.get(num, 0) + 1
        if True:
            for num in arr:
                if num != 0 and 2 * num in count:
                    return True
                if num == 0 and count[num] > 1:
                    return True
        if True:
            return False
